# 3. Label regimes (Shock/Recovery/Normal) using a 3-month recovery window
def label_regimes_vt(group, n_recovery=3):
    group = group.sort_values('date').copy()
    group['regime'] = 'Normal'
    group.loc[group['oil_shock'] == 1, 'regime'] = 'Shock'

    shock_mask = group['oil_shock'] == 1
    starts = group.index[shock_mask & (~shock_mask.shift(1, fill_value=False))]

    for start_idx in starts:
        idx_pos = group.index.get_loc(start_idx)
        end_pos = idx_pos
        # Find the end of continuous shock period
        while end_pos + 1 < len(group) and group['oil_shock'].iloc[end_pos + 1] == 1:
            end_pos += 1
        # Label subsequent months as Recovery
        for k in range(1, n_recovery + 1):
            pos = end_pos + k
            if pos >= len(group):
                break
            if group['regime'].iloc[pos] == 'Normal':
                group.iloc[pos, group.columns.get_loc('regime')] = 'Recovery'
    return group

vt_plot_df = vt_plot_df.groupby(['port', 'vessel_type_group'], group_keys=False).apply(label_regimes_vt)

# 4. Plot figure per port x vessel_type_group
for p in sorted(vt_plot_df['port'].unique()):
    port_en = port_map_en.get(p, str(p))
