        fig, ax = plt.subplots(figsize=(12, 4))
        ax.plot(sub['date'], sub['arrival_count'], color='teal', linewidth=1.5, label='Arrivals')

        # Shock and Recovery
        for regime, color, alpha in [('Shock', 'red', 0.15), ('Recovery', 'green', 0.10)]:
            mask = sub['regime'] == regime
            if not mask.any():
                continue
            group_id = (mask.ne(mask.shift(fill_value=False))).cumsum()
            for gid, gsub in sub[mask].groupby(group_id):
                ax.axvspan(gsub['date'].min(), gsub['date'].max(),
                           color=color, alpha=alpha,
                           label=regime if regime not in ax.get_legend_handles_labels()[1] else None)

