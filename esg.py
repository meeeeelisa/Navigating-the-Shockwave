"""依據 IMO 標準，定義碳排計算函數"""

import numpy as np
import pandas as pd

def estimate_vessel_co2(row):
    """
    依據 IMO (國際海事組織) 經驗公式，估算單次進港的輔機與鍋爐總碳排 (公噸)
    """
    gt = row['gross_tonnage']
    v_type = str(row['vessel_type_group']).lower()

    # 預設值（若無法辨識船型時使用）
    ae_power = 2.12 * (gt ** 0.55) if gt > 0 else 0
    load_berth = 0.4
    load_anchor = 0.2

    # 依據船型給定不同的 IMO 功率迴歸公式與負載因子
    if 'container' in v_type:          # 貨櫃船 (耗電極大)
        ae_power = 0.45 * (gt ** 0.85)
        load_berth, load_anchor = 0.55, 0.25
    elif 'bulk' in v_type:             # 散裝船
        ae_power = 2.12 * (gt ** 0.55)
        load_berth, load_anchor = 0.40, 0.20
    elif 'tanker' in v_type:           # 油輪
        ae_power = 14.7 * (gt ** 0.42)
        load_berth, load_anchor = 0.45, 0.20
    elif 'passenger' in v_type:        # 客輪/郵輪 (生活用電高)
        ae_power = 0.25 * (gt ** 0.90)
        load_berth, load_anchor = 0.60, 0.30

    # 國際標準常數
    sfc = 210           # 燃油消耗率 (g/kWh)
    cf = 3.114          # 碳轉換因子 (每噸油產生 3.114 噸 CO2)

    # 讀取時間 (若為 NaN 則補 0)
    b_hours = row['berth_wait_hours'] if pd.notna(row['berth_wait_hours']) else 0
    a_hours = row['anchor_wait_hours'] if pd.notna(row['anchor_wait_hours']) else 0

    # 分別計算碼頭與錨區的碳排放 (單位：公噸)
    co2_berth = b_hours * ae_power * load_berth * sfc * cf / 1000000
    co2_anchor = a_hours * ae_power * load_anchor * sfc * cf / 1000000

    return co2_berth + co2_anchor

# 執行計算：建立新欄位 'estimated_co2'
all_inport_df['estimated_co2'] = all_inport_df.apply(estimate_vessel_co2, axis=1)

