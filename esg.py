"""統計迴歸分析"""

import statsmodels.api as sm
import statsmodels.formula.api as smf

# 排除缺失值
regression_data = analysis_df.dropna(subset=['estimated_co2', 'oil_price', 'gross_tonnage', 'vessel_type_group', 'port']).copy()

# 建立迴歸模型，控制船隻大小(gross_tonnage)與港口結構、船型定義
# C(port) 和 C(vessel_type_group) 會自動轉為虛擬變數（控制固定效應）
model = smf.ols('estimated_co2 ~ oil_price + gross_tonnage + C(port) + C(vessel_type_group)', data=regression_data)
results = model.fit()

print(results.summary())

import statsmodels.formula.api as smf

# 排除缺失值，並確保 year 和 month 存在以控制時間固定效應
regression_data_v2 = analysis_df.dropna(
    subset=['estimated_co2', 'oil_price', 'any_shock', 'gross_tonnage', 'vessel_type_group', 'port', 'year', 'month']
).copy()

# 建立交互項迴歸模型
# oil_price * any_shock 系統會自動拆解為三個部分：
# 1. oil_price 獨立效應
# 2. any_shock 獨立效應
# 3. oil_price:any_shock 交互作用效應 (這最關鍵！)
# 同時加入 C(year) + C(month) 來控制時間固定效應，提升 R平方 (解釋力)

model_v2 = smf.ols(
    'estimated_co2 ~ oil_price * any_shock + gross_tonnage + C(port) + C(vessel_type_group) + C(year) + C(month)',
    data=regression_data_v2
)
results_v2 = model_v2.fit()

print(results_v2.summary())

